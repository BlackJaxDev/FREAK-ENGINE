namespace XREngine.Data.Rendering
{
    public enum ECullMode
    {
        /// <summary>
        /// No faces will be invisible.
        /// </summary>
        None,
        /// <summary>
        /// Back faces will be invisible.
        /// </summary>
        Back,
        /// <summary>
        /// Front faces will be invisible.
        /// </summary>
        Front,
        /// <summary>
        /// All faces will be invisible.
        /// </summary>
        Both
    }
}
