namespace XREngine.Rendering.UI
{
    public enum EListAlignment
    {
        /// <summary>
        /// Starts the list from the top or the left side of the control.
        /// </summary>
        TopOrLeft,
        /// <summary>
        /// Centers all the children in the list according to the parent control.
        /// </summary>
        Centered,
        /// <summary>
        /// Starts the list from the bottom or the right side of the control.
        /// </summary>
        BottomOrRight,
    }
}