namespace XREngine.Rendering.UI
{
    public enum EDock
    {
        /// <summary>
        /// Docks to the top of the target.
        /// </summary>
        Top,
        /// <summary>
        /// Docks to the bottom of the target.
        /// </summary>
        Bottom,
        /// <summary>
        /// Docks to the left of the target.
        /// </summary>
        Left,
        /// <summary>
        /// Docks to the right of the target.
        /// </summary>
        Right,
        /// <summary>
        /// Fills the target and becomes a tab.
        /// </summary>
        Fill
    }
}
